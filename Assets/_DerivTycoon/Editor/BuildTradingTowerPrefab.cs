
using UnityEngine;
using UnityEditor;

namespace DerivTycoon.Editor
{
    public static class BuildTradingTowerPrefab
    {
        static Material MakeMat(Color col, float metallic = 0f, float smoothness = 0.3f)
        {
            var mat = new Material(Shader.Find("Universal Render Pipeline/Lit"));
            mat.color = col;
            mat.SetFloat("_Metallic", metallic);
            mat.SetFloat("_Smoothness", smoothness);
            return mat;
        }

        static GameObject AddPrim(PrimitiveType type, string name, Transform parent,
            Vector3 localPos, Vector3 localScale, Color col,
            float metallic = 0f, float smoothness = 0.3f, Vector3? euler = null)
        {
            var go = GameObject.CreatePrimitive(type);
            go.name = name;
            go.transform.SetParent(parent);
            go.transform.localPosition = localPos;
            go.transform.localScale = localScale;
            if (euler.HasValue) go.transform.localEulerAngles = euler.Value;
            go.GetComponent<Renderer>().sharedMaterial = MakeMat(col, metallic, smoothness);
            var c = go.GetComponent<Collider>();
            if (c != null) Object.DestroyImmediate(c);
            return go;
        }

        [MenuItem("DerivTycoon/Build Prefabs/Trading Tower")]
        public static void BuildTradingTower()
        {
            // ????????? Colors ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            var cGlass      = new Color(0.20f, 0.32f, 0.42f);   // dark tinted glass
            var cGlassLight = new Color(0.35f, 0.55f, 0.70f);   // lighter glass
            var cFrame      = new Color(0.18f, 0.20f, 0.22f);   // dark steel frame
            var cFrameLight = new Color(0.32f, 0.36f, 0.40f);   // mid steel
            var cGreen      = new Color(0.08f, 0.88f, 0.45f);   // screen green
            var cGreenDim   = new Color(0.05f, 0.55f, 0.28f);   // dim screen
            var cGreenBright= new Color(0.30f, 1.00f, 0.60f);   // bright green
            var cAmber      = new Color(1.00f, 0.65f, 0.05f);   // amber ticker
            var cWhite      = new Color(0.95f, 0.96f, 0.97f);   // near white
            var cPlaza      = new Color(0.48f, 0.50f, 0.52f);   // plaza concrete
            var cAccent     = new Color(0.10f, 0.90f, 0.55f);   // teal accent
            var cAntenna    = new Color(0.72f, 0.74f, 0.76f);   // antenna metal
            var cRedLight   = new Color(1.00f, 0.10f, 0.08f);   // aviation light

            // ????????? Root ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            var root = new GameObject("TradingTowerPrefab");
            root.transform.position = new Vector3(22, 0, 0);
            var t = root.transform;

            // ????????? Plaza Base ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Plaza_Base", t,
                new Vector3(0, 0.07f, 0), new Vector3(1.90f, 0.14f, 1.90f), cPlaza, 0, 0.5f);
            // Plaza edge strips (teal)
            AddPrim(PrimitiveType.Cube, "Plaza_EdgeF", t,
                new Vector3(0, 0.15f, -0.90f), new Vector3(1.90f, 0.06f, 0.08f), cAccent, 0.2f, 0.7f);
            AddPrim(PrimitiveType.Cube, "Plaza_EdgeB", t,
                new Vector3(0, 0.15f,  0.90f), new Vector3(1.90f, 0.06f, 0.08f), cAccent, 0.2f, 0.7f);
            AddPrim(PrimitiveType.Cube, "Plaza_EdgeL", t,
                new Vector3(-0.90f, 0.15f, 0), new Vector3(0.08f, 0.06f, 1.90f), cAccent, 0.2f, 0.7f);
            AddPrim(PrimitiveType.Cube, "Plaza_EdgeR", t,
                new Vector3( 0.90f, 0.15f, 0), new Vector3(0.08f, 0.06f, 1.90f), cAccent, 0.2f, 0.7f);

            // ????????? Podium (low wide base of tower) ???????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Podium", t,
                new Vector3(0, 0.36f, 0), new Vector3(1.55f, 0.36f, 1.55f), cFrame, 0.5f, 0.4f);
            // Podium glass panels
            AddPrim(PrimitiveType.Cube, "Podium_GlassF", t,
                new Vector3(0, 0.37f, -0.76f), new Vector3(1.30f, 0.34f, 0.04f), cGlassLight, 0, 0.95f);
            AddPrim(PrimitiveType.Cube, "Podium_GlassB", t,
                new Vector3(0, 0.37f,  0.76f), new Vector3(1.30f, 0.34f, 0.04f), cGlassLight, 0, 0.95f);
            AddPrim(PrimitiveType.Cube, "Podium_GlassL", t,
                new Vector3(-0.76f, 0.37f, 0), new Vector3(0.04f, 0.34f, 1.30f), cGlassLight, 0, 0.95f);
            AddPrim(PrimitiveType.Cube, "Podium_GlassR", t,
                new Vector3( 0.76f, 0.37f, 0), new Vector3(0.04f, 0.34f, 1.30f), cGlassLight, 0, 0.95f);

            // ????????? Tower Core (main tall shaft) ???????????????????????????????????????????????????????????????????????????????????????
            // Lower section (wider)
            AddPrim(PrimitiveType.Cube, "Tower_Lower", t,
                new Vector3(0, 1.35f, 0), new Vector3(1.15f, 1.72f, 1.15f), cGlass, 0, 0.8f);
            // Upper section (stepped inward)
            AddPrim(PrimitiveType.Cube, "Tower_Upper", t,
                new Vector3(0, 2.65f, 0), new Vector3(0.88f, 0.80f, 0.88f), cGlass, 0, 0.85f);
            // Top cap
            AddPrim(PrimitiveType.Cube, "Tower_Cap", t,
                new Vector3(0, 3.10f, 0), new Vector3(0.72f, 0.20f, 0.72f), cFrame, 0.6f, 0.5f);

            // ????????? Vertical frame lines (corners, give glass curtain feel) ??????
            float[] fx = { -0.56f, 0.56f };
            float[] fz = { -0.56f, 0.56f };
            for (int i = 0; i < 2; i++)
            for (int j = 0; j < 2; j++)
            {
                AddPrim(PrimitiveType.Cube, $"Frame_V_{i}{j}", t,
                    new Vector3(fx[i], 1.35f, fz[j]), new Vector3(0.07f, 1.74f, 0.07f),
                    cFrameLight, 0.5f, 0.5f);
            }

            // Horizontal floor bands
            float[] bands = { 0.60f, 1.10f, 1.60f, 2.10f };
            for (int i = 0; i < bands.Length; i++)
            {
                AddPrim(PrimitiveType.Cube, $"Band_H_{i}", t,
                    new Vector3(0, bands[i], 0), new Vector3(1.20f, 0.06f, 1.20f), cFrameLight, 0.5f, 0.5f);
            }

            // ????????? LED Screen Band (ticker display, wraps around mid-tower) ???
            AddPrim(PrimitiveType.Cube, "Screen_F", t,
                new Vector3(0, 1.88f, -0.57f), new Vector3(1.08f, 0.22f, 0.04f), cGreen, 0, 0.9f);
            AddPrim(PrimitiveType.Cube, "Screen_B", t,
                new Vector3(0, 1.88f,  0.57f), new Vector3(1.08f, 0.22f, 0.04f), cGreenDim, 0, 0.9f);
            AddPrim(PrimitiveType.Cube, "Screen_L", t,
                new Vector3(-0.57f, 1.88f, 0), new Vector3(0.04f, 0.22f, 1.08f), cGreenDim, 0, 0.9f);
            AddPrim(PrimitiveType.Cube, "Screen_R", t,
                new Vector3( 0.57f, 1.88f, 0), new Vector3(0.04f, 0.22f, 1.08f), cGreenDim, 0, 0.9f);
            // Screen frame
            AddPrim(PrimitiveType.Cube, "Screen_FrameTop", t,
                new Vector3(0, 2.00f, -0.57f), new Vector3(1.14f, 0.05f, 0.06f), cFrame, 0.5f, 0.4f);
            AddPrim(PrimitiveType.Cube, "Screen_FrameBot", t,
                new Vector3(0, 1.76f, -0.57f), new Vector3(1.14f, 0.05f, 0.06f), cFrame, 0.5f, 0.4f);

            // ????????? Amber ticker strip (above screen) ????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Ticker_F", t,
                new Vector3(0, 2.08f, -0.57f), new Vector3(1.08f, 0.07f, 0.03f), cAmber, 0, 0.9f);
            AddPrim(PrimitiveType.Cube, "Ticker_B", t,
                new Vector3(0, 2.08f,  0.57f), new Vector3(1.08f, 0.07f, 0.03f), cAmber, 0, 0.9f);

            // ????????? Satellite Dish (on roof) ??????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cylinder, "Dish_Base", t,
                new Vector3(-0.25f, 3.22f, 0.20f), new Vector3(0.14f, 0.14f, 0.14f), cFrame, 0.5f, 0.4f);
            AddPrim(PrimitiveType.Sphere, "Dish_Bowl", t,
                new Vector3(-0.25f, 3.34f, 0.20f), new Vector3(0.32f, 0.10f, 0.32f), cAntenna, 0.5f, 0.7f,
                new Vector3(40f, 0, 0));

            // ????????? Main Antenna ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cylinder, "Antenna_Base", t,
                new Vector3(0, 3.22f, 0), new Vector3(0.08f, 0.12f, 0.08f), cFrame, 0.5f, 0.5f);
            AddPrim(PrimitiveType.Cylinder, "Antenna_Mid", t,
                new Vector3(0, 3.60f, 0), new Vector3(0.05f, 0.42f, 0.05f), cAntenna, 0.5f, 0.6f);
            AddPrim(PrimitiveType.Cylinder, "Antenna_Tip", t,
                new Vector3(0, 3.94f, 0), new Vector3(0.03f, 0.20f, 0.03f), cAntenna, 0.6f, 0.7f);
            // Aviation warning light
            AddPrim(PrimitiveType.Sphere, "Antenna_Light", t,
                new Vector3(0, 4.06f, 0), new Vector3(0.07f, 0.07f, 0.07f), cRedLight, 0, 0.9f);

            // ????????? Lobby entrance arch ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Lobby_L", t,
                new Vector3(-0.34f, 0.32f, -0.77f), new Vector3(0.10f, 0.50f, 0.06f), cFrame, 0.5f, 0.4f);
            AddPrim(PrimitiveType.Cube, "Lobby_R", t,
                new Vector3( 0.34f, 0.32f, -0.77f), new Vector3(0.10f, 0.50f, 0.06f), cFrame, 0.5f, 0.4f);
            AddPrim(PrimitiveType.Cube, "Lobby_Top", t,
                new Vector3(0, 0.59f, -0.77f), new Vector3(0.78f, 0.10f, 0.06f), cFrame, 0.5f, 0.4f);
            AddPrim(PrimitiveType.Cube, "Lobby_Door", t,
                new Vector3(0, 0.32f, -0.77f), new Vector3(0.52f, 0.46f, 0.04f), cGlassLight, 0, 0.95f);

            // ????????? Window grid on lower tower (horizontal strip detail) ???????????????
            for (int floor = 0; floor < 4; floor++)
            {
                float fy = 0.72f + floor * 0.40f;
                AddPrim(PrimitiveType.Cube, $"Win_F_{floor}", t,
                    new Vector3(0, fy, -0.57f), new Vector3(0.90f, 0.18f, 0.03f), cGlassLight, 0, 0.95f);
                AddPrim(PrimitiveType.Cube, $"Win_B_{floor}", t,
                    new Vector3(0, fy,  0.57f), new Vector3(0.90f, 0.18f, 0.03f), cGlassLight, 0, 0.95f);
            }

            // ????????? Save Prefab ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            string dir = "Assets/_DerivTycoon/Prefabs/Buildings";
            if (!AssetDatabase.IsValidFolder(dir))
                AssetDatabase.CreateFolder("Assets/_DerivTycoon/Prefabs", "Buildings");

            string path = dir + "/TradingTowerPrefab.prefab";
            PrefabUtility.SaveAsPrefabAssetAndConnect(root, path, InteractionMode.AutomatedAction);
            AssetDatabase.SaveAssets();

            Debug.Log($"[BuildPrefab] TradingTowerPrefab saved ??? {path} ({root.transform.childCount} children)");
        }
    }
}
