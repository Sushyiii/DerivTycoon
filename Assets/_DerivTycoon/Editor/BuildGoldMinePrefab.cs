            string dir = "Assets/_DerivTycoon/Prefabs/Buildings";
            if (!AssetDatabase.IsValidFolder(dir))
                AssetDatabase.CreateFolder("Assets/_DerivTycoon/Prefabs", "Buildings");

            string path = dir + "/GoldMinePrefab.prefab";
            PrefabUtility.SaveAsPrefabAssetAndConnect(root, path, InteractionMode.AutomatedAction);
            AssetDatabase.SaveAssets();

            Debug.Log($"[BuildPrefab] GoldMinePrefab saved ??? {path} ({root.transform.childCount} children)");
        }
    }
