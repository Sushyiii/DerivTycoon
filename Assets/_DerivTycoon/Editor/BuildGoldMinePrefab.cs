
using UnityEngine;
using UnityEditor;

namespace DerivTycoon.Editor
{
    public static class BuildGoldMinePrefab
    {
        static Material MakeMat(Color col, float metallic = 0f, float smoothness = 0.3f)
        {
            var mat = new Material(Shader.Find("Universal Render Pipeline/Lit"));
            mat.color = col;
            mat.SetFloat("_Metallic", metallic);
            mat.SetFloat("_Smoothness", smoothness);
            return mat;
        }

        static GameObject AddPrim(PrimitiveType type, string name, Transform parent,
            Vector3 localPos, Vector3 localScale, Color col,
            float metallic = 0f, float smoothness = 0.3f, Vector3? euler = null)
        {
            var go = GameObject.CreatePrimitive(type);
            go.name = name;
            go.transform.SetParent(parent);
            go.transform.localPosition = localPos;
            go.transform.localScale = localScale;
            if (euler.HasValue) go.transform.localEulerAngles = euler.Value;
            go.GetComponent<Renderer>().sharedMaterial = MakeMat(col, metallic, smoothness);
            // Remove physics colliders ??? we don't need them
            var col2 = go.GetComponent<Collider>();
            if (col2 != null) Object.DestroyImmediate(col2);
            return go;
        }

        [MenuItem("DerivTycoon/Build Prefabs/Gold Mine")]
        public static void BuildGoldMine()
        {
            // ????????? Colors ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            var cStone      = new Color(0.58f, 0.55f, 0.50f);
            var cWood       = new Color(0.62f, 0.48f, 0.30f);
            var cDarkWood   = new Color(0.35f, 0.24f, 0.12f);
            var cRoofSlate  = new Color(0.28f, 0.24f, 0.20f);
            var cMetal      = new Color(0.45f, 0.42f, 0.38f);
            var cGoldAccent = new Color(1.00f, 0.80f, 0.10f);
            var cPortal     = new Color(0.06f, 0.04f, 0.03f);
            var cDirt       = new Color(0.52f, 0.40f, 0.28f);
            var cRust       = new Color(0.55f, 0.30f, 0.12f);
            var cGoldOre    = new Color(0.80f, 0.65f, 0.10f);

            // ????????? Root ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            var root = new GameObject("GoldMinePrefab");
            root.transform.position = new Vector3(10, 0, 0);
            var t = root.transform;

            // ????????? Stone Foundation ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Foundation", t,
                new Vector3(0, 0.08f, 0), new Vector3(1.85f, 0.16f, 1.85f), cStone, 0, 0.15f);
            AddPrim(PrimitiveType.Cube, "DirtBase", t,
                new Vector3(0, 0.22f, 0.10f), new Vector3(1.65f, 0.12f, 1.55f), cDirt, 0, 0.1f);

            // ????????? Shaft House ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "ShaftHouse_Body", t,
                new Vector3(0, 0.90f, 0.30f), new Vector3(1.10f, 1.30f, 0.80f), cWood, 0, 0.2f);
            AddPrim(PrimitiveType.Cube, "ShaftHouse_PlankL", t,
                new Vector3(-0.56f, 0.90f, 0.30f), new Vector3(0.06f, 1.32f, 0.82f), cDarkWood, 0, 0.15f);
            AddPrim(PrimitiveType.Cube, "ShaftHouse_PlankR", t,
                new Vector3( 0.56f, 0.90f, 0.30f), new Vector3(0.06f, 1.32f, 0.82f), cDarkWood, 0, 0.15f);
            AddPrim(PrimitiveType.Cube, "ShaftHouse_Roof", t,
                new Vector3(0, 1.62f, 0.30f), new Vector3(1.30f, 0.20f, 1.00f), cRoofSlate, 0, 0.25f);
            AddPrim(PrimitiveType.Cube, "ShaftHouse_RoofRidge", t,
                new Vector3(0, 1.74f, 0.30f), new Vector3(0.18f, 0.10f, 1.02f), cDarkWood, 0, 0.2f);
            AddPrim(PrimitiveType.Cube, "ShaftHouse_Door", t,
                new Vector3(0, 0.60f, -0.09f), new Vector3(0.32f, 0.72f, 0.04f), cPortal, 0, 0.05f);
            AddPrim(PrimitiveType.Cube, "ShaftHouse_Window", t,
                new Vector3(0.30f, 1.05f, -0.09f), new Vector3(0.22f, 0.22f, 0.04f),
                new Color(0.6f, 0.8f, 1.0f), 0, 0.8f);

            // ????????? Mine Shaft Portal ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Portal_Left", t,
                new Vector3(-0.30f, 0.50f, -0.60f), new Vector3(0.18f, 1.00f, 0.28f), cStone, 0, 0.2f);
            AddPrim(PrimitiveType.Cube, "Portal_Right", t,
                new Vector3( 0.30f, 0.50f, -0.60f), new Vector3(0.18f, 1.00f, 0.28f), cStone, 0, 0.2f);
            AddPrim(PrimitiveType.Cube, "Portal_Lintel", t,
                new Vector3(0, 1.05f, -0.60f), new Vector3(0.78f, 0.20f, 0.28f), cStone, 0, 0.2f);
            AddPrim(PrimitiveType.Cube, "Portal_Void", t,
                new Vector3(0, 0.50f, -0.60f), new Vector3(0.42f, 0.90f, 0.20f), cPortal, 0, 0.05f);

            // ????????? Head Frame (A-frame hoist tower) ???????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "HeadFrame_LegL", t,
                new Vector3(-0.55f, 1.40f, -0.55f), new Vector3(0.14f, 2.40f, 0.14f), cDarkWood, 0, 0.2f,
                new Vector3(0, 0, 12f));
            AddPrim(PrimitiveType.Cube, "HeadFrame_LegR", t,
                new Vector3( 0.55f, 1.40f, -0.55f), new Vector3(0.14f, 2.40f, 0.14f), cDarkWood, 0, 0.2f,
                new Vector3(0, 0, -12f));
            AddPrim(PrimitiveType.Cube, "HeadFrame_Brace", t,
                new Vector3(0, 1.60f, -0.55f), new Vector3(0.90f, 0.10f, 0.10f), cDarkWood, 0, 0.2f);
            AddPrim(PrimitiveType.Cube, "HeadFrame_Apex", t,
                new Vector3(0, 2.55f, -0.55f), new Vector3(0.24f, 0.24f, 0.24f), cMetal, 0.4f, 0.5f);
            AddPrim(PrimitiveType.Cylinder, "HeadFrame_Pulley", t,
                new Vector3(0, 2.82f, -0.55f), new Vector3(0.22f, 0.06f, 0.22f), cMetal, 0.5f, 0.6f);
            AddPrim(PrimitiveType.Cube, "HeadFrame_Rope", t,
                new Vector3(0, 1.65f, -0.55f), new Vector3(0.03f, 1.80f, 0.03f),
                new Color(0.25f, 0.20f, 0.15f));

            // ????????? Chimney ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cylinder, "Chimney_Stack", t,
                new Vector3(-0.42f, 1.55f, 0.58f), new Vector3(0.20f, 1.10f, 0.20f), cStone, 0, 0.2f);
            AddPrim(PrimitiveType.Cylinder, "Chimney_Cap", t,
                new Vector3(-0.42f, 2.25f, 0.58f), new Vector3(0.28f, 0.07f, 0.28f), cMetal, 0.3f, 0.4f);

            // ????????? Tracks ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Track_L", t,
                new Vector3(-0.14f, 0.19f, -0.45f), new Vector3(0.05f, 0.06f, 0.75f), cMetal, 0.4f, 0.5f);
            AddPrim(PrimitiveType.Cube, "Track_R", t,
                new Vector3( 0.14f, 0.19f, -0.45f), new Vector3(0.05f, 0.06f, 0.75f), cMetal, 0.4f, 0.5f);
            for (int i = 0; i < 4; i++)
                AddPrim(PrimitiveType.Cube, $"Track_Tie_{i}", t,
                    new Vector3(0, 0.17f, -0.78f + i * 0.22f), new Vector3(0.38f, 0.05f, 0.06f), cDarkWood);

            // ????????? Mine Cart ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "MineCart_Body", t,
                new Vector3(0, 0.38f, -0.50f), new Vector3(0.36f, 0.28f, 0.42f), cRust, 0, 0.3f);
            AddPrim(PrimitiveType.Cube, "MineCart_Rim", t,
                new Vector3(0, 0.53f, -0.50f), new Vector3(0.42f, 0.06f, 0.48f), cMetal, 0.3f, 0.4f);

            var wheelPos = new Vector3[] {
                new Vector3(-0.19f, 0.22f, -0.36f), new Vector3(0.19f, 0.22f, -0.36f),
                new Vector3(-0.19f, 0.22f, -0.64f), new Vector3(0.19f, 0.22f, -0.64f),
            };
            for (int i = 0; i < 4; i++)
                AddPrim(PrimitiveType.Cylinder, $"MineCart_Wheel_{i}", t,
                    wheelPos[i], new Vector3(0.11f, 0.06f, 0.11f), cMetal, 0.5f, 0.5f,
                    new Vector3(90, 0, 0));

            AddPrim(PrimitiveType.Sphere, "Cart_GoldOre_1", t,
                new Vector3(-0.06f, 0.56f, -0.46f), new Vector3(0.14f, 0.12f, 0.14f), cGoldOre, 0.2f, 0.6f);
            AddPrim(PrimitiveType.Sphere, "Cart_GoldOre_2", t,
                new Vector3( 0.07f, 0.55f, -0.53f), new Vector3(0.11f, 0.10f, 0.11f), cGoldOre, 0.2f, 0.6f);

            // ????????? Gold Sign ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "GoldAccent_Sign", t,
                new Vector3(0, 1.30f, -0.10f), new Vector3(0.65f, 0.12f, 0.04f), cGoldAccent, 0.2f, 0.7f);

            // ????????? Rocks ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            var rockPos = new Vector3[] {
                new Vector3(-0.75f, 0.20f, -0.60f),
                new Vector3( 0.72f, 0.20f, -0.55f),
                new Vector3(-0.70f, 0.20f,  0.65f),
