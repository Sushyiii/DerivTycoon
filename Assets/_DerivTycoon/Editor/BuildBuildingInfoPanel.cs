using UnityEngine;
using UnityEngine.UI;
using UnityEditor;

namespace DerivTycoon.Editor
{
    public static class BuildBuildingInfoPanel
    {
        [MenuItem("DerivTycoon/UI/Build Building Info Panel")]
        public static void Build()
        {
            var canvas = GameObject.Find("UICanvas");
            if (canvas == null) { Debug.LogError("UICanvas not found"); return; }

            // Remove old panel if exists
            var old = canvas.transform.Find("BuildingInfoPanelRoot");
            if (old != null) Object.DestroyImmediate(old.gameObject);

            // ?????? Root panel ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            var root = new GameObject("BuildingInfoPanelRoot");
            root.transform.SetParent(canvas.transform, false);

            var rootImg = root.AddComponent<Image>();
            rootImg.color = new Color(0.08f, 0.09f, 0.12f, 0.95f);

            var rootRT = root.GetComponent<RectTransform>();
            rootRT.anchorMin        = new Vector2(1f, 0.5f);
            rootRT.anchorMax        = new Vector2(1f, 0.5f);
            rootRT.pivot            = new Vector2(1f, 0.5f);
            rootRT.anchoredPosition = new Vector2(-10f, 0f);
            rootRT.sizeDelta        = new Vector2(260f, 320f);

            root.AddComponent<DerivTycoon.UI.BuildingInfoUI>();

            // ?????? Helper: create a text child ??????????????????????????????????????????????????????????????????????????????????????????????????????
            Text MakeText(string name, string content, int fontSize, Color col,
                          Vector2 ancMin, Vector2 ancMax, Vector2 pos, Vector2 size,
                          TextAnchor align = TextAnchor.MiddleLeft)
            {
                var go = new GameObject(name);
                go.transform.SetParent(root.transform, false);
                var rt = go.AddComponent<RectTransform>();
                rt.anchorMin        = ancMin;
                rt.anchorMax        = ancMax;
                rt.pivot            = new Vector2(0.5f, 0.5f);
                rt.anchoredPosition = pos;
                rt.sizeDelta        = size;
                var txt = go.AddComponent<Text>();
                txt.text      = content;
                txt.fontSize  = fontSize;
                txt.color     = col;
                txt.alignment = align;
                txt.font      = Resources.GetBuiltinResource<Font>("LegacyRuntime.ttf");
                return txt;
            }

            Button MakeButton(string name, string label, Color bgCol,
                              Vector2 ancMin, Vector2 ancMax, Vector2 pos, Vector2 size)
            {
                var go = new GameObject(name);
                go.transform.SetParent(root.transform, false);
                var rt = go.AddComponent<RectTransform>();
                rt.anchorMin        = ancMin;
                rt.anchorMax        = ancMax;
                rt.pivot            = new Vector2(0.5f, 0.5f);
                rt.anchoredPosition = pos;
                rt.sizeDelta        = size;
                var img = go.AddComponent<Image>();
                img.color = bgCol;
                var btn = go.AddComponent<Button>();

                var lblGO = new GameObject("Text");
                lblGO.transform.SetParent(go.transform, false);
                var lblRT = lblGO.AddComponent<RectTransform>();
                lblRT.anchorMin = Vector2.zero;
                lblRT.anchorMax = Vector2.one;
                lblRT.sizeDelta = Vector2.zero;
                var lblTxt = lblGO.AddComponent<Text>();
                lblTxt.text      = label;
                lblTxt.fontSize  = 14;
                lblTxt.color     = Color.white;
                lblTxt.alignment = TextAnchor.MiddleCenter;
                lblTxt.font      = Resources.GetBuiltinResource<Font>("LegacyRuntime.ttf");

                return btn;
            }

            // ?????? Close button (top-right X) ?????????????????????????????????????????????????????????????????????????????????????????????????????????
            MakeButton("CloseButton", "???",
                new Color(0.55f, 0.15f, 0.12f),
                new Vector2(1f, 1f), new Vector2(1f, 1f),
                new Vector2(-16f, -16f), new Vector2(28f, 28f));

            // ?????? Building name (title) ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            MakeText("BuildingNameText", "Gold Mine",
                18, new Color(1f, 0.85f, 0.2f),
                new Vector2(0f, 1f), new Vector2(1f, 1f),
                new Vector2(0f, -36f), new Vector2(-10f, 32f),
                TextAnchor.MiddleCenter);

            // ?????? Divider label ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            MakeText("SymbolText", "frxXAUUSD",
                11, new Color(0.5f, 0.55f, 0.6f),
                new Vector2(0f, 1f), new Vector2(1f, 1f),
                new Vector2(0f, -68f), new Vector2(-10f, 20f),
                TextAnchor.MiddleCenter);

            // ?????? Price info ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            MakeText("StakeText", "Stake: $100.00",
                13, new Color(0.75f, 0.78f, 0.82f),
                new Vector2(0f, 1f), new Vector2(1f, 1f),
                new Vector2(8f, -104f), new Vector2(-16f, 22f));

            MakeText("EntryPriceText", "Entry:   $0.00000",
                13, new Color(0.75f, 0.78f, 0.82f),
                new Vector2(0f, 1f), new Vector2(1f, 1f),
                new Vector2(8f, -130f), new Vector2(-16f, 22f));

            MakeText("CurrentPriceText", "Current: $0.00000",
                13, new Color(0.75f, 0.78f, 0.82f),
                new Vector2(0f, 1f), new Vector2(1f, 1f),
                new Vector2(8f, -156f), new Vector2(-16f, 22f));

            // ?????? P&L ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            MakeText("PnLText", "P&L: --",
                15, new Color(0.25f, 0.95f, 0.35f),
                new Vector2(0f, 1f), new Vector2(1f, 1f),
                new Vector2(0f, -192f), new Vector2(-10f, 28f),
                TextAnchor.MiddleCenter);

            // ?????? Sell Mine button ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            MakeButton("SellButton", "Sell Mine",
                new Color(0.75f, 0.18f, 0.12f),
                new Vector2(0f, 0f), new Vector2(1f, 0f),
                new Vector2(0f, 30f), new Vector2(-20f, 44f));

            // ?????? Wire up BuildingInfoUI fields ????????????????????????????????????????????????????????????????????????????????????????????????
            var ui = root.GetComponent<DerivTycoon.UI.BuildingInfoUI>();
            ui.PanelRoot         = root;
            ui.BuildingNameText  = root.transform.Find("BuildingNameText") .GetComponent<Text>();
            ui.SymbolText        = root.transform.Find("SymbolText")       .GetComponent<Text>();
            ui.StakeText         = root.transform.Find("StakeText")        .GetComponent<Text>();
            ui.EntryPriceText    = root.transform.Find("EntryPriceText")   .GetComponent<Text>();
            ui.CurrentPriceText  = root.transform.Find("CurrentPriceText") .GetComponent<Text>();
            ui.PnLText           = root.transform.Find("PnLText")          .GetComponent<Text>();
            ui.SellButton        = root.transform.Find("SellButton")       .GetComponent<Button>();
            ui.CloseButton       = root.transform.Find("CloseButton")      .GetComponent<Button>();

            // ?????? Wire HUD reference ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            // (no HUD reference needed for this panel)

            EditorUtility.SetDirty(canvas);
            UnityEditor.SceneManagement.EditorSceneManager.MarkSceneDirty(
                UnityEngine.SceneManagement.SceneManager.GetActiveScene());

            Debug.Log("[BuildBuildingInfoPanel] BuildingInfoPanelRoot created and wired.");
        }
    }
}
