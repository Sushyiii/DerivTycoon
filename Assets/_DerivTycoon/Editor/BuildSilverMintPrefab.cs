
using UnityEngine;
using UnityEditor;

namespace DerivTycoon.Editor
{
    public static class BuildSilverMintPrefab
    {
        static Material MakeMat(Color col, float metallic = 0f, float smoothness = 0.3f)
        {
            var mat = new Material(Shader.Find("Universal Render Pipeline/Lit"));
            mat.color = col;
            mat.SetFloat("_Metallic", metallic);
            mat.SetFloat("_Smoothness", smoothness);
            return mat;
        }

        static GameObject AddPrim(PrimitiveType type, string name, Transform parent,
            Vector3 localPos, Vector3 localScale, Color col,
            float metallic = 0f, float smoothness = 0.3f, Vector3? euler = null)
        {
            var go = GameObject.CreatePrimitive(type);
            go.name = name;
            go.transform.SetParent(parent);
            go.transform.localPosition = localPos;
            go.transform.localScale = localScale;
            if (euler.HasValue) go.transform.localEulerAngles = euler.Value;
            go.GetComponent<Renderer>().sharedMaterial = MakeMat(col, metallic, smoothness);
            var c = go.GetComponent<Collider>();
            if (c != null) Object.DestroyImmediate(c);
            return go;
        }

        [MenuItem("DerivTycoon/Build Prefabs/Silver Mint")]
        public static void BuildSilverMint()
        {
            // ????????? Colors ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            var cMarble     = new Color(0.94f, 0.94f, 0.92f);   // white marble
            var cMarbleDark = new Color(0.75f, 0.75f, 0.73f);   // shadow marble
            var cSilver     = new Color(0.80f, 0.82f, 0.85f);   // polished silver
            var cSilverDark = new Color(0.55f, 0.57f, 0.60f);   // dark silver
            var cGold       = new Color(0.90f, 0.75f, 0.20f);   // gold trim
            var cPortal     = new Color(0.08f, 0.07f, 0.10f);   // dark door/window
            var cRoof       = new Color(0.40f, 0.42f, 0.45f);   // slate grey roof
            var cGlass      = new Color(0.65f, 0.80f, 0.95f);   // glass
            var cCoin       = new Color(0.85f, 0.87f, 0.90f);   // silver coin

            // ????????? Root ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            var root = new GameObject("SilverMintPrefab");
            root.transform.position = new Vector3(13, 0, 0);
            var t = root.transform;

            // ????????? Marble Stepped Base ???????????????????????????????????????????????????????????????????????????????????????????????????????????????
            // Step 3 (bottom, widest)
            AddPrim(PrimitiveType.Cube, "Step_Bottom", t,
                new Vector3(0, 0.07f, 0), new Vector3(1.90f, 0.14f, 1.90f), cMarble, 0, 0.5f);
            // Step 2
            AddPrim(PrimitiveType.Cube, "Step_Mid", t,
                new Vector3(0, 0.21f, 0), new Vector3(1.72f, 0.14f, 1.72f), cMarble, 0, 0.5f);
            // Step 1 (top, narrowest)
            AddPrim(PrimitiveType.Cube, "Step_Top", t,
                new Vector3(0, 0.35f, 0), new Vector3(1.55f, 0.14f, 1.55f), cMarble, 0, 0.5f);

            // ????????? Main Building Body ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Building_Body", t,
                new Vector3(0, 1.05f, 0), new Vector3(1.40f, 1.20f, 1.30f), cMarble, 0, 0.4f);

            // ????????? Columns (front face ??? 4 columns) ????????????????????????????????????????????????????????????????????????
            float colY = 1.00f;
            float colH = 1.10f;
            float[] colX = { -0.52f, -0.18f, 0.18f, 0.52f };
            for (int i = 0; i < 4; i++)
            {
                // Column shaft
                AddPrim(PrimitiveType.Cylinder, $"Col_Shaft_{i}", t,
                    new Vector3(colX[i], colY, -0.66f), new Vector3(0.13f, colH * 0.5f, 0.13f),
                    cMarble, 0, 0.6f);
                // Column capital (top cube)
                AddPrim(PrimitiveType.Cube, $"Col_Capital_{i}", t,
                    new Vector3(colX[i], 1.86f, -0.66f), new Vector3(0.19f, 0.10f, 0.19f),
                    cMarble, 0, 0.5f);
                // Column base
                AddPrim(PrimitiveType.Cube, $"Col_Base_{i}", t,
                    new Vector3(colX[i], 0.48f, -0.66f), new Vector3(0.17f, 0.08f, 0.17f),
                    cMarbleDark, 0, 0.4f);
            }

            // ????????? Entablature (beam across top of columns) ???????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Entablature", t,
                new Vector3(0, 1.98f, -0.66f), new Vector3(1.42f, 0.16f, 0.22f), cMarble, 0, 0.45f);
            AddPrim(PrimitiveType.Cube, "Frieze", t,
                new Vector3(0, 2.08f, -0.63f), new Vector3(1.38f, 0.10f, 0.16f), cMarbleDark, 0, 0.3f);

            // ????????? Triangular Pediment ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            // Simulate with 3 thin cubes forming a low triangle
            AddPrim(PrimitiveType.Cube, "Pediment_L", t,
                new Vector3(-0.52f, 2.25f, -0.64f), new Vector3(0.55f, 0.08f, 0.14f),
                cMarble, 0, 0.4f, new Vector3(0, 0, 22f));
            AddPrim(PrimitiveType.Cube, "Pediment_R", t,
                new Vector3( 0.52f, 2.25f, -0.64f), new Vector3(0.55f, 0.08f, 0.14f),
                cMarble, 0, 0.4f, new Vector3(0, 0, -22f));
            AddPrim(PrimitiveType.Cube, "Pediment_Base", t,
                new Vector3(0, 2.14f, -0.64f), new Vector3(1.42f, 0.07f, 0.14f), cMarble, 0, 0.4f);

            // ????????? Gabled Roof ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Roof_Body", t,
                new Vector3(0, 2.32f, 0.10f), new Vector3(1.46f, 0.22f, 1.10f), cRoof, 0, 0.3f);
            AddPrim(PrimitiveType.Cube, "Roof_Ridge", t,
                new Vector3(0, 2.46f, 0.10f), new Vector3(0.20f, 0.10f, 1.12f), cSilverDark, 0, 0.4f);

            // ????????? Main Entrance Door ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Door_Frame", t,
                new Vector3(0, 0.86f, -0.66f), new Vector3(0.46f, 0.94f, 0.06f), cMarbleDark, 0, 0.3f);
            AddPrim(PrimitiveType.Cube, "Door_Void", t,
                new Vector3(0, 0.84f, -0.66f), new Vector3(0.34f, 0.80f, 0.06f), cPortal, 0, 0.1f);

            // ????????? Windows (side faces) ???????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "Window_L1", t,
                new Vector3(-0.71f, 1.10f, -0.10f), new Vector3(0.06f, 0.30f, 0.32f), cGlass, 0, 0.9f);
            AddPrim(PrimitiveType.Cube, "Window_R1", t,
                new Vector3( 0.71f, 1.10f, -0.10f), new Vector3(0.06f, 0.30f, 0.32f), cGlass, 0, 0.9f);

            // ????????? Gold trim band ?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cube, "GoldTrim_Front", t,
                new Vector3(0, 1.68f, -0.65f), new Vector3(1.38f, 0.06f, 0.04f), cGold, 0.3f, 0.8f);
            AddPrim(PrimitiveType.Cube, "GoldTrim_Back", t,
                new Vector3(0, 1.68f, 0.66f), new Vector3(1.38f, 0.06f, 0.04f), cGold, 0.3f, 0.8f);

            // ????????? Silver Ingot Stack (display outside) ???????????????????????????????????????????????????????????????
            // 3x2 stack of ingots on left side of steps
            for (int row = 0; row < 2; row++)
            for (int col = 0; col < 3; col++)
            {
                AddPrim(PrimitiveType.Cube, $"Ingot_{row}_{col}", t,
                    new Vector3(-0.75f + col * 0.16f, 0.52f + row * 0.14f, 0.55f),
                    new Vector3(0.14f, 0.10f, 0.22f), cSilver, 0.6f, 0.9f);
            }

            // ????????? Decorative urns on top corners ??????????????????????????????????????????????????????????????????????????????
            AddPrim(PrimitiveType.Cylinder, "Urn_L", t,
                new Vector3(-0.60f, 2.60f, 0.55f), new Vector3(0.15f, 0.22f, 0.15f), cMarble, 0, 0.5f);
            AddPrim(PrimitiveType.Sphere,   "Urn_L_Top", t,
                new Vector3(-0.60f, 2.76f, 0.55f), new Vector3(0.18f, 0.14f, 0.18f), cMarble, 0, 0.5f);
            AddPrim(PrimitiveType.Cylinder, "Urn_R", t,
                new Vector3( 0.60f, 2.60f, 0.55f), new Vector3(0.15f, 0.22f, 0.15f), cMarble, 0, 0.5f);
            AddPrim(PrimitiveType.Sphere,   "Urn_R_Top", t,
                new Vector3( 0.60f, 2.76f, 0.55f), new Vector3(0.18f, 0.14f, 0.18f), cMarble, 0, 0.5f);

            // ????????? Silver coin stack (front steps, right side) ??????????????????????????????????????????
            for (int i = 0; i < 5; i++)
            {
                AddPrim(PrimitiveType.Cylinder, $"Coin_{i}", t,
                    new Vector3(0.60f, 0.44f + i * 0.08f, -0.52f),
                    new Vector3(0.20f, 0.04f, 0.20f), cCoin, 0.7f, 0.9f);
            }

            // ????????? Save Prefab ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
            string dir = "Assets/_DerivTycoon/Prefabs/Buildings";
            if (!AssetDatabase.IsValidFolder(dir))
                AssetDatabase.CreateFolder("Assets/_DerivTycoon/Prefabs", "Buildings");

            string path = dir + "/SilverMintPrefab.prefab";
            PrefabUtility.SaveAsPrefabAssetAndConnect(root, path, InteractionMode.AutomatedAction);
            AssetDatabase.SaveAssets();

            Debug.Log($"[BuildPrefab] SilverMintPrefab saved ??? {path} ({root.transform.childCount} children)");
        }
    }
}
